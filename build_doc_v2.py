GOLD  = RGBColor(0xC9, 0xA8, 0x4C)
DARK  = RGBColor(0x1A, 0x1A, 0x1A)
WHITE = RGBColor(0xFF, 0xFF, 0xFF)
GREY  = RGBColor(0x6B, 0x6B, 0x6B)
RED   = RGBColor(0xDC, 0x26, 0x26)
GREEN = RGBColor(0x16, 0xA3, 0x4A)
BLUE  = RGBColor(0x25, 0x63, 0xEB)

def rtl(p):
    pPr = p._p.get_or_add_pPr()
    b = OxmlElement('w:bidi'); b.set(qn('w:val'), '1')
    pPr.append(b)

def cell_bg(cell, hex_color):
    tc = cell._tc; tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear'); shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color); tcPr.append(shd)

def heading(doc, text, level=1):
    p = doc.add_paragraph(); rtl(p)
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    r = p.add_run(text); r.bold = True; r.font.name = 'Arial'
    r.font.color.rgb = GOLD if level == 1 else DARK
    r.font.size = Pt(17 if level == 1 else 13 if level == 2 else 11)
    p.paragraph_format.space_before = Pt(18 if level == 1 else 12 if level == 2 else 6)
    p.paragraph_format.space_after  = Pt(5)
    return p

def body(doc, text, bold=False, color=None, indent_cm=0):
    p = doc.add_paragraph(); rtl(p)
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    r = p.add_run(text); r.font.name = 'Arial'
    r.font.size = Pt(11); r.bold = bold
    r.font.color.rgb = color if color else DARK
    p.paragraph_format.space_after = Pt(4)
    if indent_cm:
        p.paragraph_format.right_indent = Cm(indent_cm)
    return p

def bullet(doc, text, color=None):
    p = doc.add_paragraph(style='List Bullet'); rtl(p)
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    r = p.add_run(text); r.font.name = 'Arial'
    r.font.size = Pt(11); r.font.color.rgb = color if color else DARK
    p.paragraph_format.space_after = Pt(3)
    return p

def note_box(doc, text, color=BLUE):
    p = doc.add_paragraph(); rtl(p)
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    r = p.add_run('ملاحظة: ' + text)
    r.font.name = 'Arial'; r.font.size = Pt(10)
    r.font.color.rgb = color; r.italic = True
    p.paragraph_format.space_after = Pt(6)
    p.paragraph_format.right_indent = Cm(0.5)

def warning_box(doc, text):
    note_box(doc, text, color=RED)

def divider(doc):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_before = Pt(2); p.paragraph_format.space_after = Pt(2)
    r = p.add_run('─' * 62); r.font.color.rgb = GOLD; r.font.size = Pt(7)

def table(doc, headers, rows, header_bg='1A1A1A', first_col_bold=False):
    t = doc.add_table(rows=1+len(rows), cols=len(headers))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER; t.style = 'Table Grid'
    for i, h in enumerate(headers):
        c = t.rows[0].cells[i]; cell_bg(c, header_bg)
        p = c.paragraphs[0]; p.alignment = WD_ALIGN_PARAGRAPH.CENTER; rtl(p)
        r = p.add_run(h); r.bold = True; r.font.name = 'Arial'
        r.font.size = Pt(10); r.font.color.rgb = WHITE
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            c = t.rows[ri+1].cells[ci]
            if ri % 2 == 0: cell_bg(c, 'F8F5ED')
            p = c.paragraphs[0]; p.alignment = WD_ALIGN_PARAGRAPH.RIGHT; rtl(p)
            r = p.add_run(val); r.font.name = 'Arial'; r.font.size = Pt(10)
            r.font.color.rgb = DARK
            if first_col_bold and ci == 0: r.bold = True
    col_w = Inches(6.5) / len(headers)
    for col in t.columns:
        for c in col.cells: c.width = col_w
    doc.add_paragraph()
    return t

def section_card(doc, num, ar_name, en_name, path, who, desc, requires, steps=None):
    """Renders a full section block with heading, description, requirements, steps."""
    heading(doc, f'٣.{num}  {ar_name}  ({en_name})', level=2)
    # Path badge
    p = doc.add_paragraph(); rtl(p); p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    r1 = p.add_run('المسار: '); r1.font.name='Arial'; r1.font.bold=True; r1.font.size=Pt(10); r1.font.color.rgb=GOLD
    r2 = p.add_run(path); r2.font.name='Arial'; r2.font.size=Pt(10); r2.font.color.rgb=GREY
    p.paragraph_format.space_after = Pt(4)
    # Who
    p2 = doc.add_paragraph(); rtl(p2); p2.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    r3 = p2.add_run('من يستخدمه: '); r3.font.name='Arial'; r3.font.bold=True; r3.font.size=Pt(10); r3.font.color.rgb=GOLD
    r4 = p2.add_run(who); r4.font.name='Arial'; r4.font.size=Pt(10); r4.font.color.rgb=GREY
    p2.paragraph_format.space_after = Pt(5)
    # Description
    body(doc, desc)
    # Requires
    heading(doc, 'ما المطلوب لكي يعمل هذا القسم؟', level=3)
    for req in requires:
        bullet(doc, req)
    # Steps
    if steps:
        heading(doc, 'كيفية الاستخدام:', level=3)
        for i, s in enumerate(steps, 1):
            body(doc, f'{i}. {s}', indent_cm=0.8)
    doc.add_paragraph()


